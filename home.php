<script>
document.addEventListener('DOMContentLoaded', function() {
    const submitCommentBtn = document.querySelector('.submit-comment-btn');
    const toggleCommentsBtn = document.querySelector('.toggle-comments-btn');
    const commentInput = document.querySelector('.comment-input');
    const commentsContainer = document.querySelector('.comments');
    const commentCount = document.querySelector('.comment-count');

    submitCommentBtn.addEventListener('click', function() {
        const commentText = commentInput.value.trim();
        if (commentText !== '') {
            addComment(commentText);
            commentInput.value = '';
        } else {
            alert('Please enter your comment.');
        }
    });

    toggleCommentsBtn.addEventListener('click', function() {
        commentsContainer.classList.toggle('hidden');
        if (commentsContainer.classList.contains('hidden')) {
            toggleCommentsBtn.textContent = 'View Comments';
        } else {
            toggleCommentsBtn.textContent = 'Hide Comments';
        }
    });

    function addComment(commentText) {
        const currentDate = new Date();
        const commentTimestamp = currentDate.toLocaleString('en-US', { weekday: 'long', year: 'numeric', month: 'long', day: 'numeric', hour: 'numeric', minute: 'numeric', second: 'numeric' });

        const commentDiv = document.createElement('div');
        commentDiv.classList.add('comment');

        const commentInfo = document.createElement('div');
        commentInfo.classList.add('comment-info');
        const commentTextElement = document.createElement('p');
        commentTextElement.classList.add('comment-text');
        commentTextElement.textContent = commentText;
        const commentTimestampElement = document.createElement('span');
        commentTimestampElement.textContent = commentTimestamp;

        commentInfo.appendChild(commentTextElement);
        commentInfo.appendChild(commentTimestampElement);

        commentDiv.appendChild(commentInfo);

        commentsContainer.appendChild(commentDiv);

        updateCommentCount();
    }

    function updateCommentCount() {
        const commentElements = commentsContainer.querySelectorAll('.comment');
        const commentCountValue = commentElements.length;
        commentCount.textContent = `${commentCountValue} ${commentCountValue === 1 ? 'comment' : 'comments'}`;
    }
});
</script>
<script>
  document.getElementById("searchForm").addEventListener("submit", function(event) {
    event.preventDefault();
    const query = document.getElementById("searchInput").value;
    fetch("search.php?query=" + encodeURIComponent(query))
      .then(response => response.json())
      .then(results => displayResults(results));
  });

  function displayResults(results) {
    const searchResults = document.getElementById("searchResults");
    searchResults.innerHTML = "";
    results.forEach(result => {
      const li = document.createElement("li");
      li.textContent = result.title;
      searchResults.appendChild(li);
    });
  }
</script>
 
