            hideCommentsBtn.style.display = 'block';
            viewCommentsBtn.style.display = 'none';
        } else {
            alert('Please enter your name and comment.');
        }
    });

    function addComment(userName, commentText) {
        const commentDiv = document.createElement('div');
        commentDiv.classList.add('comment');

        const userProfilePic = document.createElement('div');
        userProfilePic.classList.add('user-profile-pic');
        userProfilePic.textContent = userName.charAt(0).toUpperCase();
        commentDiv.appendChild(userProfilePic);

        const commentContent = document.createElement('div');
        commentContent.classList.add('comment-content');

        const commentHeader = document.createElement('div');
        commentHeader.classList.add('comment-header');
        const userNameElement = document.createElement('span');
        userNameElement.textContent = userName;
        commentHeader.appendChild(userNameElement);
        const timestamp = document.createElement('span');
        timestamp.classList.add('timestamp');
        timestamp.textContent = getTimestamp();
        commentHeader.appendChild(timestamp);
        commentContent.appendChild(commentHeader);

        const commentTextElement = document.createElement('p');
        commentTextElement.classList.add('comment-text');
        commentTextElement.textContent = commentText;
        commentContent.appendChild(commentTextElement);

