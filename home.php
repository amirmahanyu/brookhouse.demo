    submitCommentBtn.addEventListener('click', function() {
        const userName = userNameInput.value.trim();
        const commentText = commentInput.value.trim();

        if (userName !== '' && commentText !== '') {
            addComment(userName, commentText);
            userNameInput.value = '';
            commentInput.value = '';
