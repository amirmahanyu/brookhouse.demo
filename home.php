        likeBtn.classList.add('like-btn');
        likeBtn.textContent = 'Like';
        commentContent.appendChild(likeBtn);

        const likeCounter = document.createElement('span');
        likeCounter.classList.add('like-counter');
        likeCounter.textContent = '0';
        commentContent.appendChild(likeCounter);

        const replyTextarea = document.createElement('textarea');
        replyTextarea.classList.add('reply-textarea');
        replyTextarea.placeholder = 'Write a reply...';
        commentContent.appendChild(replyTextarea);

        const submitReplyBtn = document.createElement('button');
        submitReplyBtn.classList.add('submit-reply-btn');
        submitReplyBtn.textContent = 'Submit Reply';
        commentContent.appendChild(submitReplyBtn);

        commentDiv.appendChild(commentContent);
        commentsContainer.appendChild(commentDiv);

        bindEvents(commentDiv);
    }

    function bindEvents(commentDiv) {
        const likeBtn = commentDiv.querySelector('.like-btn');
        const likeCounter = commentDiv.querySelector('.like-counter');

        likeBtn.addEventListener('click', function() {
            let count = parseInt(likeCounter.textContent);
            count++;
            likeCounter.textContent = count;
        });

        const submitReplyBtn = commentDiv.querySelector('.submit-reply-btn');
        const replyTextarea = commentDiv.querySelector('.reply-textarea');

        submitReplyBtn.addEventListener('click', function() {
            const replyText = replyTextarea.value.trim();
            if (replyText !== '') {
                const replyParagraph = document.createElement('p');
                replyParagraph.classList.add('reply');
                replyParagraph.textContent = replyText;
                commentDiv.appendChild(replyParagraph);
                replyTextarea.value = '';
            } else {
                alert('Please enter your reply.');
            }
        });
    }

    function getTimestamp() {
        const now = new Date();
        const options = { weekday: 'short', year: 'numeric', month: 'short', day: 'numeric', hour: 'numeric', minute: 'numeric', second: 'numeric', timeZone: 'Africa/Nairobi' };
        return now.toLocaleDateString('en-US', options);
    }

    viewCommentsBtn.addEventListener('click', function() {
