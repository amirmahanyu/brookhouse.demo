        hideCommentsBtn.style.display = 'block';
    });

    hideCommentsBtn.addEventListener('click', function() {
        commentsContainer.style.display = 'none';
        viewCommentsBtn.style.display = 'block';
        hideCommentsBtn.style.display = 'none';
    });
});
